package hk.edu.cuhk.ie.iems5722.a2_1155164913;

public class ChatroomItem {

    private int id;
    private String name;

    public ChatroomItem(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}